        <h1>Sobre nosotros</h1>

        <h2>¿Quienes somos?</h2>
        <p>Somos estudiantes de la Escuela de Educación Técnica N.°6 del Partido de La Matanza, una institución que no solo nos forma académicamente, sino que también nos impulsa a pensar de forma crítica, trabajar en equipo y buscar soluciones creativas a problemas reales de nuestra comunidad.
Durante nuestra formación técnica, adquirimos conocimientos en programación, diseño web, análisis de datos y gestión de la información, y decidimos aplicar todo lo aprendido en un proyecto que pueda generar un impacto positivo y duradero.
Nos une la convicción de que la tecnología y la educación son herramientas fundamentales para construir un futuro mejor. Creemos que, con esfuerzo y dedicación, podemos transformar la manera en que las familias acceden a la información y contribuir a que más jóvenes encuentren oportunidades en el ámbito educativo y profesional.</p>
        <h2>Nuestro Objetivo</h2>
        <p>Con OrientaTec, nuestro objetivo principal es facilitar el acceso a información clara, completa y confiable sobre las escuelas técnicas secundarias del Partido de La Matanza. Sabemos que, para muchas familias, elegir la institución educativa adecuada puede ser un proceso complicado debido a la falta de datos centralizados, actualizados y fáciles de entender.
Queremos que esta plataforma sea una herramienta de apoyo para los padres, madres y estudiantes que buscan continuar su formación técnica. A través de un mapa interactivo y fichas informativas de cada escuela, OrientaTec les permitirá comparar especialidades, ubicaciones, recursos, contactos y características de cada institución.
Nuestro propósito es reducir las barreras de acceso a la información educativa y contribuir a una distribución más equitativa de la matrícula escolar. Creemos que, al brindar información organizada y accesible, ayudamos a que cada estudiante pueda elegir el camino que mejor se adapte a sus intereses y sueños.</p>
        <h2>¿Como lo conseguimos?</h2>
        <p>Este proyecto nació como parte de nuestra formación en la Técnica 6, pero va mucho más allá de una tarea escolar: es el resultado de meses de aprendizaje, investigación y trabajo en equipo.

Aplicamos todo lo que aprendimos en áreas como:

Programación y desarrollo web: para construir una página intuitiva, moderna y funcional.

Diseño de interfaces y experiencia de usuario: para que la navegación sea simple y atractiva.

Bases de datos y organización de la información: para integrar datos actualizados sobre cada escuela técnica.

Comunicación y trabajo colaborativo: para unir nuestras ideas, distribuir responsabilidades y cumplir los objetivos del proyecto.

Gracias a este aprendizaje, creamos OrientaTec, una plataforma que refleja todo el conocimiento que adquirimos y que, al mismo tiempo, nos permitió desarrollar habilidades prácticas que serán fundamentales para nuestro futuro profesional.

Para nosotros, OrientaTec no es solo una página web: es un puente entre la educación y la comunidad, un proyecto pensado para ayudar a las familias y, sobre todo, para demostrar que desde la escuela también se pueden generar soluciones reales e innovadoras.</p>
